    private void ClickOrdenar(java.awt.event.ActionEvent evt) {                              
        jTextArea1.setText("");
        Collections.sort(theList);
        int size = theList.size();
        for(int a=0; a<size; a++){
            
            jTextArea1.append(theList.get(a).toString() + "\n");
        }
    }                             

